    private function apply_style($name, $style)
    {
        $name = strtolower($name);
        $styled = '';
        for ($i = 0; $i < strlen($name); $i++) {
            $styled .= $style[$name[$i]] ?? $name[$i];
        }
        return $styled;
    }

    public function style($name, $styleName)
    {
        if (!isset($this->styles[$styleName])) {
            return $name;  // Return original name if style not found
        }
        return $this->apply_style($name, $this->styles[$styleName]);
    }
